namespace LoreMaster.Enums;

/// <summary>
/// Represents the rank of the power.
/// </summary>
public enum PowerRank
{
    /// <summary>
    /// The power is very powerful.
    /// </summary>
    Greater,

    /// <summary>
    /// The power does grant a major benefit.
    /// </summary>
    Medium,

    /// <summary>
    /// The power is a nice small improvement.
    /// </summary>
    Lower,

    /// <summary>
    /// The power is always on as it just provides access to something.
    /// </summary>
    Permanent,

    /// <summary>
    /// For placeholders
    /// </summary>
    None
}
